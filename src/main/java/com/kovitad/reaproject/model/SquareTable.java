package com.kovitad.reaproject.model;

public class SquareTable {

	int dx,dy;
	public SquareTable(int dxUnits, int dyUnits){
		this.dx = dxUnits;
		this.dy = dyUnits;
		
	}
	public int getDx() {
		return dx;
	}
	public void setDx(int dx) {
		this.dx = dx;
	}
	public int getDy() {
		return dy;
	}

	public void setDy(int dy) {
		this.dy = dy;
	}

}
